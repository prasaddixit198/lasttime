CREATE DATABASE FTP87
USE FTP87

DROP TABLE EMPLOYEE_DET IF EXISTS 

CREATE TABLE EMPLOYEE_DET
(
EMP_ID INT NOT NULL PRIMARY KEY,
EMP_NAME VARCHAR(30),
DEPARTMENT VARCHAR(30),
EMAIL VARCHAR(30),
MOBILE_NO VARCHAR(30),
DATE_OF_JOINING DATE,
LEAVE_BAL INT,
MANAGER_ID INT,
FOREIGN KEY(MANAGER_ID) REFERENCES EMPLOYEE_DET(EMP_ID)
)

SELECT * FROM EMPLOYEE_DET


CREATE TABLE LEAVEDETAILS_DET
(
LEAVE_ID INT AUTO_INCREMENT PRIMARY KEY,
LEAVE_SDATE DATE,
LEAVE_EDATE DATE,
LEAVE_NDAYS INT,
LEAVE_TYPE ENUM('EL','SL','CL'),
LEAVE_STATUS ENUM('PENDING','APPROVED','DENIED'),
LEAVE_REASON VARCHAR(100),
LEAVE_APPLIEDON DATE,
LEAVE_MGRCOMM VARCHAR(100),
FOREIGN KEY(LEAVE_ID) REFERENCES EMPLOYEE_DET(EMP_ID)
)


SELECT * FROM LEAVEDETAILS_DET

